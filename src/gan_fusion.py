import os
import torch
import torch.nn as nn
import torch.optim as optim
import torchvision.transforms as transforms
from torch.utils.data import Dataset, DataLoader
from PIL import Image
import numpy as np

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print("Using device:", device)

class FusionDataset(Dataset):
    def __init__(self, ct_dir, mri_dir):
        self.ct_dir = ct_dir
        self.mri_dir = mri_dir

        self.ct_images = sorted(os.listdir(ct_dir))
        self.mri_images = sorted(os.listdir(mri_dir))

        self.transform = transforms.Compose([
            transforms.Resize((256, 256)),
            transforms.ToTensor(),
            transforms.Normalize((0.5,), (0.5,))  # Normalize to [-1,1]
        ])

    def __len__(self):
        return len(self.ct_images)

    def __getitem__(self, idx):
        ct_path = os.path.join(self.ct_dir, self.ct_images[idx])
        mri_path = os.path.join(self.mri_dir, self.mri_images[idx])

        ct = Image.open(ct_path).convert("L")
        mri = Image.open(mri_path).convert("L")

        ct = self.transform(ct)
        mri = self.transform(mri)

        return ct, mri

class Generator(nn.Module):
    def __init__(self):
        super(Generator, self).__init__()

        self.model = nn.Sequential(
            nn.Conv2d(2, 64, 3, 1, 1),
            nn.ReLU(True),

            nn.Conv2d(64, 128, 3, 1, 1),
            nn.BatchNorm2d(128),
            nn.ReLU(True),

            nn.Conv2d(128, 64, 3, 1, 1),
            nn.BatchNorm2d(64),
            nn.ReLU(True),

            nn.Conv2d(64, 1, 3, 1, 1),
            nn.Tanh()
        )

    def forward(self, ct, mri):
        x = torch.cat([ct, mri], dim=1)
        return self.model(x)

class Discriminator(nn.Module):
    def __init__(self):
        super(Discriminator, self).__init__()

        self.model = nn.Sequential(
            nn.Conv2d(1, 64, 4, 2, 1),
            nn.LeakyReLU(0.2, inplace=True),

            nn.Conv2d(64, 128, 4, 2, 1),
            nn.BatchNorm2d(128),
            nn.LeakyReLU(0.2, inplace=True),

            nn.Conv2d(128, 1, 4, 1, 1)
        )

    def forward(self, x):
        return self.model(x)

adversarial_loss = nn.BCEWithLogitsLoss()
l1_loss = nn.L1Loss()

def gradient_loss(fused, target):
    fx = fused[:, :, :, :-1] - fused[:, :, :, 1:]
    fy = fused[:, :, :-1, :] - fused[:, :, 1:, :]

    tx = target[:, :, :, :-1] - target[:, :, :, 1:]
    ty = target[:, :, :-1, :] - target[:, :, 1:, :]

    return l1_loss(fx, tx) + l1_loss(fy, ty)

dataset = FusionDataset("/content/CT-MRI/CT", "/content/CT-MRI/MRI")
loader = DataLoader(dataset, batch_size=4, shuffle=True)

G = Generator().to(device)
D = Discriminator().to(device)

optimizer_G = optim.Adam(G.parameters(), lr=0.0002, betas=(0.5, 0.999))
optimizer_D = optim.Adam(D.parameters(), lr=0.0002, betas=(0.5, 0.999))

epochs = 100

for epoch in range(epochs):
    for ct, mri in loader:

        ct = ct.to(device)
        mri = mri.to(device)

        # --------------------
        # Train Generator
        # --------------------
        optimizer_G.zero_grad()

        fused = G(ct, mri)
        pred_fake = D(fused)

        real = torch.ones_like(pred_fake)
        fake = torch.zeros_like(pred_fake)

        adv_loss = adversarial_loss(pred_fake, real)

        l1_ct = l1_loss(fused, ct)
        l1_mri = l1_loss(fused, mri)

        grad_mri = gradient_loss(fused, mri)

        g_loss = (
            1.0 * adv_loss +
            5.0 * l1_ct +
            5.0 * l1_mri +
            2.0 * grad_mri
        )

        g_loss.backward()
        optimizer_G.step()

        # --------------------
        # Train Discriminator
        # --------------------
        optimizer_D.zero_grad()

        real_loss = adversarial_loss(D(ct), real)
        fake_loss = adversarial_loss(D(fused.detach()), fake)

        d_loss = (real_loss + fake_loss) / 2

        d_loss.backward()
        optimizer_D.step()

    print(f"Epoch [{epoch+1}/{epochs}]  G_loss: {g_loss.item():.4f}  D_loss: {d_loss.item():.4f}")

