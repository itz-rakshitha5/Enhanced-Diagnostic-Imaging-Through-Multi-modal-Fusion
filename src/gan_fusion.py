import os
import numpy as np
import pandas as pd
from PIL import Image
from math import log10
from skimage.metrics import structural_similarity as ssim

# --------------------------
# Paths
# --------------------------
ct_dir = "/content/CT-MRI/CT"
mri_dir = "/content/CT-MRI/MRI"

ct_images = sorted([f for f in os.listdir(ct_dir) if f.endswith(".png")])
mri_images = sorted([f for f in os.listdir(mri_dir) if f.endswith(".png")])

# --------------------------
# Image loader (GAN compatible)
# --------------------------
def load_image(path):
    img = Image.open(path).convert("L")
    img = img.resize((256, 256))
    img = np.array(img).astype(np.float32) / 255.0
    return img

# --------------------------
# Metrics
# --------------------------
def entropy(img):
    hist, _ = np.histogram(img.flatten(), bins=256, range=(0, 1))
    hist = hist / (hist.sum() + 1e-10)
    return -np.sum(hist * np.log2(hist + 1e-10))

def psnr(img1, img2):
    mse = np.mean((img1 - img2) ** 2)
    if mse == 0:
        return 100
    return 20 * log10(1.0 / np.sqrt(mse))

def rmse(img1, img2):
    return np.sqrt(np.mean((img1 - img2) ** 2))

def mutual_information(x, y):
    hist_2d, _, _ = np.histogram2d(
        x.flatten(), y.flatten(), bins=64, range=[[0,1],[0,1]]
    )
    pxy = hist_2d / np.sum(hist_2d)
    px = np.sum(pxy, axis=1)
    py = np.sum(pxy, axis=0)
    px_py = px[:, None] * py[None, :]
    return np.sum(pxy * np.log2((pxy + 1e-10) / (px_py + 1e-10)))

# --------------------------
# GAN Evaluation
# --------------------------
metrics_data = []
G.eval()

for i in range(10):  # images 16003–16012
    ct_path = os.path.join(ct_dir, ct_images[i])
    mri_path = os.path.join(mri_dir, mri_images[i])

    ct = load_image(ct_path)
    mri = load_image(mri_path)

    ct_t = torch.tensor(ct).unsqueeze(0).unsqueeze(0).to(device)
    mri_t = torch.tensor(mri).unsqueeze(0).unsqueeze(0).to(device)

    with torch.no_grad():
        fused = G(ct_t, mri_t).squeeze().cpu().numpy()

    # Normalize GAN output (Tanh → [0,1])
    fused = (fused + 1) / 2
    fused = np.clip(fused, 0, 1)

    metrics_data.append([
        ct_images[i],
        entropy(fused),
        psnr(fused, ct),
        ssim(fused, ct, data_range=1.0),
        mutual_information(fused, ct),
        rmse(fused, ct)
    ])

